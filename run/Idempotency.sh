#!/bin/bash
#==============================================================================
GET_XS=0;
GET_GECO=0;
RUN_XS=1;
RUN_GECO=1;
#==============================================================================
###############################################################################
#==============================================================================
# GET XS
if [[ "$GET_XS" -eq "1" ]]; then
  rm -fr XS
  git clone https://github.com/pratas/XS.git
  cd XS/
  make
  cp XS ../XSx
  cd ..
  rm -fr XS/
  mv XSx XS
fi
#==============================================================================
# GET GECO
if [[ "$GET_GECO" -eq "1" ]]; then
  git clone https://github.com/pratas/geco.git
  cd geco/src/
  cmake .
  make
  cp GeCo ../../
  cd ../../
  rm -fr geco/
fi
#==============================================================================
###############################################################################
#==============================================================================
# RUN XS
if [[ "$RUN_XS" -eq "1" ]]; then
  rm -f SYN*;
  for((x=1,y=1 ; x<10000000 ; x*=10, ++y)); do
  ./XS -v -ls 1000 -n $x -eh -eo -es -edb -f 0.25,0.25,0.25,0.25,0.0 -rn 0 -rm 0 -s 0 SYN$y;
  done
fi
#==============================================================================
# RUN GECO
if [[ "$RUN_GECO" -eq "1" ]]; then
  for((x=1 ; x<8 ; ++x)); do
    echo "Sequence $x:";
    ./GeCo -tm 20:500:0:0/10 -c 30 -g 0.9 SYN$x;
    cat SYN$x SYN$x > SYNXX$x
    ./GeCo -tm 20:500:0:0/10 -c 30 -g 0.9 SYNXX$x;
  done
fi
#==============================================================================
~                                      
